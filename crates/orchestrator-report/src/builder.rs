/// Pull concise evidence strings from `debate_state_artifact.debate_turns`
/// filtered by role side ("bull" or "bear"). Each turn's `artifact.report`
/// (or `per_ticker[].key_evidence`) is mined for text.
fn extract_debate_evidence(debate: &Value, side: &str) -> Vec<String> {
    let mut evidence: Vec<String> = Vec::new();
    let turns = debate.get("debate_turns").and_then(Value::as_array);
    if let Some(turns) = turns {
        for turn in turns {
            let role = turn.get("role").and_then(Value::as_str).unwrap_or("");
            if !role.to_ascii_lowercase().contains(side) {
                continue;
            }
            let artifact = turn.get("artifact").unwrap_or(&Value::Null);
            // Prefer key_evidence arrays on per-ticker payloads.
            if let Some(per_ticker) = artifact.get("per_ticker").and_then(Value::as_object) {
                for data in per_ticker.values() {
                    if let Some(items) = data.get("key_evidence").and_then(Value::as_array) {
                        for item in items {
                            if let Some(text) = item.as_str() {
                                evidence.push(text.to_string());
                            }
                        }
                    }
                }
            }
            // Fall back to the report prose (first non-empty line).
            if evidence.is_empty() {
                if let Some(report) = artifact.get("report").and_then(Value::as_str) {
                    let first_line = report
                        .lines()
                        .map(str::trim)
                        .find(|line| !line.is_empty())
                        .unwrap_or("");
                    if !first_line.is_empty() {
                        evidence.push(first_line.to_string());
                    }
                }
            }
            if evidence.len() >= 3 {
                break;
            }
        }
    }
    evidence
}

/// Collect per-evidence source-quality and ticker-level consensus-risk
/// metadata for one debate side ("bull" / "bear"). Returns a Markdown-ready
/// string (empty when no structured quality data is present). Defensive:
/// missing fields are skipped, plain-string evidence is ignored.
fn collect_evidence_quality(debate: &Value, side: &str) -> String {
    let mut lines: Vec<String> = Vec::new();
    if let Some(turns) = debate.get("debate_turns").and_then(Value::as_array) {
        for turn in turns {
            let role = turn.get("role").and_then(Value::as_str).unwrap_or("");
            if !role.to_ascii_lowercase().contains(side) {
                continue;
            }
            let artifact = turn.get("artifact").unwrap_or(&Value::Null);
            if let Some(per_ticker) = artifact.get("per_ticker").and_then(Value::as_object) {
                for data in per_ticker.values() {
                    for field in ["echo_chamber_risk", "crowded_consensus_risk"] {
                        if let Some(value) = data.get(field).and_then(Value::as_str) {
                            if !value.trim().is_empty() {
                                lines.push(format!("- {}: {}", field.replace('_', " "), value));
                            }
                        }
                    }
                    if let Some(items) = data.get("key_evidence").and_then(Value::as_array) {
                        for item in items {
                            if let Some(obj) = item.as_object() {
                                let tier =
                                    obj.get("source_tier").and_then(Value::as_str).unwrap_or("");
                                let first = obj
                                    .get("first_source")
                                    .and_then(Value::as_str)
                                    .unwrap_or("");
                                let mut bits: Vec<String> = Vec::new();
                                if !tier.is_empty() {
                                    bits.push(format!("source tier: {tier}"));
                                }
                                if !first.is_empty() {
                                    bits.push(format!("first source: {first}"));
                                }
                                if let Some(true) =
                                    obj.get("is_derivative_repost").and_then(Value::as_bool)
                                {
                                    bits.push("derivative repost".to_string());
                                }
                                if !bits.is_empty() {
                                    let claim =
                                        obj.get("claim").and_then(Value::as_str).unwrap_or("");
                                    lines.push(format!("- {claim} ({})", bits.join(", ")));
                                }
                            }
                        }
                    }
                }
            }
        }
    }
    lines.join("\n")
}

fn risk_field_str<'a>(rc: &'a Value, field: &str) -> Option<&'a str> {
    rc.get(field)
        .and_then(Value::as_str)
        .filter(|value| !value.trim().is_empty())
}

fn risk_field_pct(rc: &Value, field: &str) -> Option<String> {
    rc.get(field)
        .and_then(Value::as_f64)
        .filter(|value| *value != 0.0)
        .map(|value| format!("{:.0}%", value * 100.0))
}

fn risk_assessment(risk_state: &Value, allocation: &Value) -> String {
    let vix_regime = allocation
        .get("vix_regime")
        .and_then(Value::as_str)
        .unwrap_or("N/A");

    // History entries store the artifact under `.artifact` (see contract.rs).
    let history = risk_state
        .get("history")
        .and_then(Value::as_array)
        .cloned()
        .unwrap_or_default();

    // `recommended_adjustment` lives at the top level in some flows and inside
    // the last history artifact in others — check both.
    let recommended = risk_state
        .get("recommended_adjustment")
        .and_then(Value::as_str)
        .or_else(|| {
            history
                .last()
                .and_then(|entry| entry.get("artifact").or(Some(entry)))
                .and_then(|artifact| artifact.get("recommended_adjustment"))
                .and_then(Value::as_str)
        })
        .unwrap_or("None");

    let mut section = format!(
        "## 5. Risk Assessment\n\n\
        **VIX Regime:** {vix_regime}\n\
        **Recommended Adjustment:** {recommended}\n\n\
        Key risks identified:\n"
    );

    let mut risks: Vec<String> = Vec::new();

    for entry in &history {
        let artifact = entry.get("artifact").unwrap_or(entry);
        // Prefer structured risk lists when available.
        let mut found_structured = false;
        if let Some(key_risks) = artifact.get("key_risks").and_then(Value::as_array) {
            for risk in key_risks {
                if let Some(text) = risk.as_str() {
                    risks.push(text.to_string());
                    found_structured = true;
                }
            }
        }
        if let Some(balanced) = artifact.get("balanced_view").and_then(Value::as_array) {
            for view in balanced {
                if let Some(text) = view.as_str() {
                    risks.push(text.to_string());
                    found_structured = true;
                }
            }
        }
        // Fall back to the RiskConstraints `argument` prose for this entry.
        if !found_structured {
            if let Some(argument) = artifact.get("argument").and_then(Value::as_str) {
                if !argument.trim().is_empty() {
                    risks.push(argument.to_string());
                }
            }
        }
    }

    // Direct RiskConstraints (no history wrapper).
    if risks.is_empty() {
        if let Some(argument) = risk_state.get("argument").and_then(Value::as_str) {
            if !argument.trim().is_empty() {
                risks.push(argument.to_string());
            }
        }
    }
    // FinalValidation.risk_controls is a Vec<String> of risk controls.
    if risks.is_empty() {
        if let Some(controls) = risk_state.get("risk_controls").and_then(Value::as_array) {
            for control in controls {
                if let Some(text) = control.as_str() {
                    risks.push(text.to_string());
                }
            }
        }
    }

    if risks.is_empty() {
        section.push_str("- No specific risks identified.\n");
    } else {
        for risk in risks.iter().take(5) {
            section.push_str(&format!("- {}\n", risk));
        }
    }

    // Surface structured RiskConstraints fields when present. These live at
    // the top level of `risk_debate_state` or inside the last history
    // artifact in some flows. Missing fields are skipped.
    let rc = history
        .last()
        .map(|entry| entry.get("artifact").unwrap_or(entry))
        .unwrap_or(risk_state);
    let structured: Vec<String> = vec![
        risk_field_str(rc, "stop_type").map(|value| format!("- **Stop Type:** {value}")),
        risk_field_pct(rc, "max_drawdown_pct").map(|value| format!("- **Max Drawdown:** {value}")),
        risk_field_pct(rc, "position_cap_pct").map(|value| format!("- **Position Cap:** {value}")),
        risk_field_str(rc, "rebalance_trigger")
            .map(|value| format!("- **Rebalance Trigger:** {value}")),
        risk_field_str(rc, "risk_off_trigger")
            .map(|value| format!("- **Risk-Off Trigger:** {value}")),
        risk_field_str(rc, "review_window").map(|value| format!("- **Review Window:** {value}")),
        risk_field_str(rc, "cash_hedge_recommendation")
            .map(|value| format!("- **Cash Hedge:** {value}")),
        risk_field_pct(rc, "constraint_confidence")
            .map(|value| format!("- **Constraint Confidence:** {value}")),
    ]
    .into_iter()
    .flatten()
    .collect();
    if !structured.is_empty() {
        section.push_str("\n**Structured Risk Constraints:**\n");
        for line in structured {
            section.push_str(&format!("{line}\n"));
        }
    }
    section
}

fn trade_plan(trader: &Value, final_decision: &Value) -> String {
    let action = trader
        .get("action")
        .and_then(Value::as_str)
        .or_else(|| final_decision.get("rating").and_then(Value::as_str))
        .unwrap_or("N/A");
    let position_size = trader
        .get("position_size")
        .and_then(Value::as_str)
        .unwrap_or("N/A");
    // TradeIntent exposes `entry_price` / `stop_loss`; accept `entry` / `stop`
    // aliases for forward compatibility with the plan template.
    let entry = trader
        .get("entry_price")
        .and_then(Value::as_str)
        .or_else(|| trader.get("entry").and_then(Value::as_str))
        .unwrap_or("N/A");
    let stop = trader
        .get("stop_loss")
        .and_then(Value::as_str)
        .or_else(|| trader.get("stop").and_then(Value::as_str))
        .unwrap_or("N/A");
    let horizon = final_decision
        .get("horizon")
        .and_then(Value::as_str)
        .unwrap_or("N/A");

    format!(
        "## 6. Trade Plan\n\n\
        | Parameter | Value |\n|-----------|-------|\n\
        | Action | {action} |\n\
        | Position Size | {position_size} |\n\
        | Entry | {entry} |\n\
        | Stop Loss | {stop} |\n\
        | Horizon | {horizon} |\n"
    )
}

fn portfolio_allocation(allocation: &Value) -> String {
    let mut section = String::from("## 7. Portfolio Allocation\n\n");
    let weights = allocation.get("weights").and_then(Value::as_object);

    if let Some(weights) = weights {
        if weights.is_empty() {
            section.push_str("No allocation data available.\n");
        } else {
            section.push_str("| Asset | Weight | Rationale |\n|-------|--------|-----------|\n");
            for (asset, data) in weights {
                let weight = data.get("weight").and_then(Value::as_f64).unwrap_or(0.0);
                let rationale = data.get("rationale").and_then(Value::as_str).unwrap_or("");
                section.push_str(&format!(
                    "| {} | {:.1}% | {} |\n",
                    asset,
                    weight * 100.0,
                    rationale
                ));
            }
        }
    } else {
        section.push_str("No allocation data available.\n");
    }

    let total = allocation.get("total_equity_exposure");
    let total_str = match total {
        Some(Value::Number(n)) => format_pct(&Value::Number(n.clone())),
        Some(value) => format_pct(value),
        None => "N/A".to_string(),
    };
    let corr = allocation
        .get("correlation_note")
        .and_then(Value::as_str)
        .unwrap_or("");
    let summary = allocation
        .get("summary")
        .and_then(Value::as_str)
        .unwrap_or("");

    section.push_str(&format!(
        "\n**Total Equity Exposure:** {}\n**Correlation Note:** {}\n\n{}\n",
        total_str, corr, summary
    ));
    section
}

fn confidence_and_limitations(research: &Value) -> String {
    let mut section = String::from("## 8. Confidence & Limitations\n\n");

    let data_gaps = research
        .get("data_gaps")
        .and_then(Value::as_array)
        .cloned()
        .unwrap_or_default();
    let validation_triggers = research
        .get("validation_triggers")
        .and_then(Value::as_array)
        .cloned()
        .unwrap_or_default();

    section.push_str("**Data Gaps:**\n");
    if data_gaps.is_empty() {
        section.push_str("- None identified.\n");
    } else {
        for gap in &data_gaps {
            if let Some(text) = gap.as_str() {
                section.push_str(&format!("- {}\n", text));
            }
        }
    }

    section.push_str("\n**Validation Triggers:**\n");
    if validation_triggers.is_empty() {
        section.push_str("- None identified.\n");
    } else {
        for trigger in &validation_triggers {
            if let Some(text) = trigger.as_str() {
                section.push_str(&format!("- {}\n", text));
            }
        }
    }
    section
}

fn next_steps(research: &Value) -> String {
    let mut section =
        String::from("## 9. Next Steps\n\nObservation checklist from research manager:\n");
    let plan = research.get("plan").and_then(Value::as_str).or_else(|| {
        research
            .get("observation_checklist")
            .and_then(Value::as_str)
    });

    if let Some(plan_text) = plan {
        // Split plan text into bullet points if it contains newlines or semicolons.
        let items: Vec<&str> = plan_text
            .split('\n')
            .flat_map(|s| s.split(';'))
            .map(str::trim)
            .filter(|s| !s.is_empty())
            .collect();
        if items.is_empty() {
            section.push_str(&format!("- {}\n", plan_text));
        } else {
            for item in items.iter().take(10) {
                section.push_str(&format!("- {}\n", item));
            }
        }
    } else {
        section.push_str("- No observation checklist provided.\n");
    }
    section
}

/// Format a probability/exposure value as a percentage.
///
/// Values in the `[0.0, 1.0]` range are multiplied by 100; values above `1.0`
/// are treated as already being on a 0-100 scale. String values are passed
/// through unchanged.
fn format_pct(value: &Value) -> String {
    match value {
        Value::Number(n) => {
            let f = n.as_f64().unwrap_or(0.0);
            if f > 1.0 {
                format!("{:.0}%", f)
            } else {
                format!("{:.0}%", f * 100.0)
            }
        }
        Value::String(s) => s.to_string(),
        _ => "N/A".to_string(),
    }
}

/// Convert a Markdown report into a standalone, styled HTML document.
///
/// Uses `pulldown-cmark` for spec-compliant CommonMark parsing and wraps the
/// fragment in a full `<!doctype html>` document with inline CSS so it renders
/// correctly in email clients.
pub fn report_to_html(markdown: &str) -> String {
    // Enable table and strikethrough extensions so the Trade Plan and Portfolio
    // Allocation Markdown tables render as proper <table> elements.
    let mut options = Options::empty();
    options.insert(Options::ENABLE_TABLES);
    options.insert(Options::ENABLE_STRIKETHROUGH);
    let parser = Parser::new_ext(markdown, options);
    let mut body = String::new();
    html::push_html(&mut body, parser);

    let title = markdown
        .lines()
        .find_map(|line| line.strip_prefix("# ").map(str::trim))
        .unwrap_or("Strategy Report");

    format!(
        "<!doctype html><html><head><meta charset=\"utf-8\"><title>{title}</title>\
        <style>{css}</style></head><body>{body}</body></html>",
        title = encode_text(title),
        css = REPORT_CSS,
        body = body
    )
}

