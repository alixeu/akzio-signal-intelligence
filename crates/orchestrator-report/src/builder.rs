/// Build a structured human-readable Markdown report from state artifacts.
///
/// Every section degrades gracefully when its source artifact is missing —
/// placeholders ("N/A", "No data available") are emitted instead of panicking.
pub fn build_human_readable_report(state: &Value) -> String {
    let date = state
        .get("current_date")
        .and_then(Value::as_str)
        .or_else(|| state.get("date").and_then(Value::as_str))
        .unwrap_or("N/A");
    let ticker = state
        .get("ticker")
        .and_then(Value::as_str)
        .or_else(|| {
            state
                .get("tickers")
                .and_then(Value::as_array)
                .and_then(|items| items.first())
                .and_then(Value::as_str)
        })
        .unwrap_or("N/A");
    let timestamp = Local::now().format("%Y-%m-%d %H:%M:%S").to_string();

    let research = state.get("research_plan").unwrap_or(&Value::Null);
    let trader = state.get("trader_investment_plan").unwrap_or(&Value::Null);
    let final_decision = state.get("final_trade_decision").unwrap_or(&Value::Null);
    let allocation = state.get("portfolio_allocation").unwrap_or(&Value::Null);
    let risk_state = state.get("risk_debate_state").unwrap_or(&Value::Null);
    let debate = state.get("debate_state_artifact").unwrap_or(&Value::Null);

    let mut report = String::new();
    report.push_str(&format!("# {} Strategy Report — {}\n\n", ticker, date));

    // Section 1: Executive Summary
    report.push_str(&executive_summary(research, trader, final_decision));
    report.push_str("\n---\n\n");

    // Section 2: Investment Thesis
    report.push_str(&investment_thesis(research, final_decision));
    report.push_str("\n---\n\n");

    // Section 3: Bull Case
    report.push_str(&bull_case(debate));
    report.push_str("\n---\n\n");

    // Section 4: Bear Case
    report.push_str(&bear_case(debate));
    report.push_str("\n---\n\n");

    // Section 5: Risk Assessment
    report.push_str(&risk_assessment(risk_state, allocation));
    report.push_str("\n---\n\n");

    // Section 6: Trade Plan
    report.push_str(&trade_plan(trader, final_decision));
    report.push_str("\n---\n\n");

    // Section 7: Portfolio Allocation
    report.push_str(&portfolio_allocation(allocation));
    report.push_str("\n---\n\n");

    // Section 8: Confidence & Limitations
    report.push_str(&confidence_and_limitations(research));
    report.push_str("\n---\n\n");

    // Section 9: Next Steps
    report.push_str(&next_steps(research));
    report.push_str(&format!(
        "\n---\n*Generated by Akzio Signal Intelligence | {}*\n",
        timestamp
    ));

    report
}

fn executive_summary(research: &Value, trader: &Value, final_decision: &Value) -> String {
    let rating = research
        .get("rating")
        .and_then(Value::as_str)
        .or_else(|| final_decision.get("rating").and_then(Value::as_str))
        .unwrap_or("N/A");
    let long_prob = research
        .get("long_probability")
        .map(format_pct)
        .unwrap_or_else(|| "N/A".to_string());
    let short_prob = research
        .get("short_probability")
        .map(format_pct)
        .unwrap_or_else(|| "N/A".to_string());
    let rationale = research
        .get("probability_rationale")
        .and_then(Value::as_str)
        .unwrap_or("");
    let dominant_driver = research
        .get("dominant_driver")
        .and_then(Value::as_str)
        .unwrap_or("N/A");
    let why_now = research
        .get("why_now")
        .and_then(Value::as_str)
        .unwrap_or("N/A");
    let action = trader
        .get("action")
        .and_then(Value::as_str)
        .unwrap_or("N/A");
    let confidence_basis = research
        .get("confidence_basis")
        .and_then(Value::as_str)
        .filter(|value| !value.trim().is_empty());
    let hold_reason = research
        .get("hold_reason")
        .and_then(Value::as_str)
        .filter(|value| !value.trim().is_empty());
    let mut basis_summary = String::new();
    if let Some(value) = confidence_basis {
        basis_summary.push_str(&format!("Confidence basis: {value}\n"));
    }
    if let Some(value) = hold_reason {
        basis_summary.push_str(&format!("Hold reason: {value}\n"));
    }

    format!(
        "## 1. Executive Summary\n\n\
        Current rating: **{rating}** (long: {long_prob}, short: {short_prob}).\n\
        {basis_summary}\
        {rationale}\n\n\
        **Dominant Driver:** {dominant_driver}\n\
        **Why Now:** {why_now}\n\n\
        **Recommended Action:** {action}\n"
    )
}

fn investment_thesis(research: &Value, final_decision: &Value) -> String {
    let rating = final_decision
        .get("rating")
        .and_then(Value::as_str)
        .or_else(|| research.get("rating").and_then(Value::as_str))
        .unwrap_or("N/A");
    let horizon = final_decision
        .get("horizon")
        .and_then(Value::as_str)
        .unwrap_or("N/A");
    let long_prob = research
        .get("long_probability")
        .map(format_pct)
        .unwrap_or_else(|| "N/A".to_string());
    let short_prob = research
        .get("short_probability")
        .map(format_pct)
        .unwrap_or_else(|| "N/A".to_string());
    let rationale = research
        .get("probability_rationale")
        .and_then(Value::as_str)
        .unwrap_or("");
    let thesis = final_decision
        .get("investment_thesis")
        .and_then(Value::as_str)
        .filter(|text| !text.trim().is_empty())
        .unwrap_or("");

    let mut section = format!(
        "## 2. Investment Thesis\n\n\
        Rating: **{rating}** | Horizon: {horizon}\n\
        Probability: Long {long_prob} / Short {short_prob}\n\n\
        {rationale}\n"
    );
    if !thesis.is_empty() {
        section.push_str(&format!("\n{thesis}\n"));
    }
    section
}

fn bull_case(debate: &Value) -> String {
    let mut section = String::from("## 3. Bull Case\n\nTop bull evidence from debate:\n");

    // Forward-compatible direct field.
    if let Some(evidence) = debate.get("bull_evidence").and_then(Value::as_array) {
        if !evidence.is_empty() {
            for (i, evidence) in evidence.iter().take(3).enumerate() {
                let text = evidence
                    .as_str()
                    .or_else(|| evidence.get("point").and_then(Value::as_str))
                    .unwrap_or("(no description)");
                section.push_str(&format!("{}. {}\n", i + 1, text));
            }
            return section;
        }
    }

    // Extract from debate turns whose role contains "bull".
    let evidence = extract_debate_evidence(debate, "bull");
    if evidence.is_empty() {
        section.push_str("- No bull evidence recorded.\n");
    } else {
        for (i, text) in evidence.iter().take(3).enumerate() {
            section.push_str(&format!("{}. {}\n", i + 1, text));
        }
    }
    let quality = collect_evidence_quality(debate, "bull");
    if !quality.is_empty() {
        section.push_str("\n\n**Source Quality:**\n");
        section.push_str(&quality);
    }
    section
}

